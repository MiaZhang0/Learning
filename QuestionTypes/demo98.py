#使用二维列表描述9x9围棋局面，‘w’表示白色棋子，‘b’表示黑色棋子，‘ -’表示无子，打印成下图左所示的文本棋盘。
phase = [
    ['-','-','-','-','-','-','-','-','-'],
    ['-','-','-','-','-','-','-','-','-'],
    ['-','w','-','-','-','-','b','-','-'],
    ['-','-','-','-','-','-','-','-','-'],
    ['-','-','-','-','-','-','-','-','-'],
    ['-','-','-','-','-','-','-','-','-'],
    ['-','w','-','-','-','-','b','-','-'],
    ['-','-','-','-','-','-','-','-','-'],
    ['-','-','-','-','-','-','-','-','-'],
]
def print_go(phase):
    print('+-------------------+')
    for row in phase:
        print('| ',end='')
        for col in row:
            print('%s '%col,end='')
        print('|')
    print('+-------------------+')
print_go(phase)